#include <iostream>

using namespace std;

int main() {
    int width, height;

    // Запрашиваем у пользователя ширину и высоту рамки
    cout << "Введите ширину и высоту рамки (через пробел): ";
    cin >> width >> height;

    // Проверка на положительность ширины и высоты
    if (width < 2 || height < 2) {
        cout << "Ширина и высота должны быть больше или равны 2." << endl;
        return 1; // Завершение программы с ошибкой
    }

    // Рисуем верхнюю часть рамки
    for (int i = 0; i < width; ++i) {
        cout << "-"; // Рисуем горизонтальные линии
    }
    cout << endl; // Переход на следующую строку

    // Рисуем боковые стороны рамки
    for (int i = 0; i < height - 2; ++i) {
        cout << "|"; // Левая сторона рамки
        for (int j = 0; j < width - 2; ++j) {
            cout << " "; // Пустое пространство внутри рамки
        }
        cout << "|"; // Правая сторона рамки
        cout << endl; // Переход на следующую строку
    }

    // Рисуем нижнюю часть рамки
    for (int i = 0; i < width; ++i) {
        cout << "-"; // Рисуем горизонтальные линии
    }
    cout << endl; // Переход на следующую строку

    return 0;
}